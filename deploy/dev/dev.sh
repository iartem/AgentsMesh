    echo "  前端:       http://localhost:$WEB_PORT"
    echo "  API:        http://localhost:$HTTP_PORT/api"
    echo "  Relay:      ws://localhost:$HTTP_PORT/relay (via Traefik)"
    echo ""
    echo "  测试账号:   dev@agentsmesh.local / devpass123"
    echo "  管理员:     admin@agentsmesh.local / adminpass123"
    echo ""
    echo "  其他服务:"
    echo "    Gitea:    http://localhost:$GITEA_HTTP_PORT (gitea-admin / gitea-admin-123)"
    echo "    Traefik:  http://localhost:$TRAEFIK_DASHBOARD_PORT (Dashboard)"
    echo "    Adminer:  http://localhost:$ADMINER_PORT"
    echo "    MinIO:    http://localhost:$MINIO_CONSOLE_PORT"
    echo "    gRPC:     grpcs://localhost:$GRPC_PORT (mTLS)"
    echo ""
    echo "  停止: ./dev.sh --clean"
    echo ""
}

# 启动本地前端
start_frontend() {
    source "$ENV_FILE"
    local web_dir="$SCRIPT_DIR/../../web"
    local web_port="${WEB_PORT:-3000}"

    # 检查是否已有前端进程在运行
    if lsof -i :"$web_port" &>/dev/null; then
        warn "端口 $web_port 已被占用，跳过前端启动"
        return 0
    fi

    # 检查 pnpm
    if ! command -v pnpm &>/dev/null; then
        error "未找到 pnpm，请先安装: npm install -g pnpm"
        return 1
    fi

    # 检查依赖
    if [[ ! -d "$web_dir/node_modules" ]]; then
        info "安装前端依赖..."
        (cd "$web_dir" && pnpm install --frozen-lockfile) || {
            error "前端依赖安装失败"
            return 1
        }
        success "前端依赖安装完成"
    fi

    # 启动前端（后台运行，日志输出到文件）
    local log_file="$SCRIPT_DIR/web.log"
    info "启动前端服务 (端口: $web_port)..."
    (cd "$web_dir" && nohup pnpm dev --turbopack --port "$web_port" > "$log_file" 2>&1 &)

    # 等待前端启动
    local max_wait=30
    for ((i=1; i<=max_wait; i++)); do
        if curl -s "http://localhost:$web_port" &>/dev/null; then
            success "前端服务已启动 (http://localhost:$web_port)"
            return 0
        fi
        sleep 1
    done

    warn "前端服务启动中，请稍后访问 http://localhost:$web_port"
    echo "  查看日志: tail -f $log_file"
}

# 主流程
main() {
    cd "$SCRIPT_DIR"

    # 处理 --clean 参数
    if [[ "${1:-}" == "--clean" || "${1:-}" == "-c" ]]; then
        clean
        exit 0
    fi

    # 显示帮助
    if [[ "${1:-}" == "--help" || "${1:-}" == "-h" ]]; then
        echo "用法: $0 [选项]"
        echo ""
        echo "选项:"
        echo "  无参数    一键启动完整开发环境（Docker 后端 + 本地前端）"
        echo "  --clean   停止并清理所有服务"
        echo ""
        echo "前端日志: tail -f deploy/dev/web.log"
        exit 0
    fi

    echo ""
    echo "=========================================="
    echo "  AgentsMesh 开发环境初始化"
    echo "=========================================="
    echo ""

    # Step 1: 生成 SSL 证书
    generate_ssl_certs

    # Step 2: 生成 AI CLI 配置
    generate_ai_cli_configs

    # Step 3: 生成配置
    generate_env
    source "$ENV_FILE"

    # Step 4: 生成 Traefik 配置（动态网络名，解决多 worktree 路由冲突）
    generate_traefik_config

    # Step 5: 生成 web/.env.local（支持本地前端开发）
    generate_web_env

    # Step 6: 启动 Docker 后端服务
    info "启动 Docker 服务 (首次可能需要几分钟)..."
    docker compose up -d --build --quiet-pull 2>&1 | grep -v "^#" | grep -v "^\[" | grep -v "^$" || true
    success "Docker 服务已启动"

    # Step 7: 等待 PostgreSQL
    local pg_container="${COMPOSE_PROJECT_NAME}-postgres-1"
    info "等待 PostgreSQL 就绪..."
    if ! wait_for_service "$pg_container" "pg_isready -U agentsmesh"; then
        error "PostgreSQL 启动超时"
        exit 1
    fi
    success "PostgreSQL 已就绪"

    # Step 8: 执行迁移（使用 backend 容器中的 migrate 工具）
    run_migrations

    # Step 9: 初始化 seed
    init_seed "$pg_container"

    # Step 9.5: 初始化 Gitea Git 服务器
    init_gitea

    # Step 10: 修复 workspace 权限 (runner 容器)
    local runner_container="${COMPOSE_PROJECT_NAME}-runner-1"
    docker exec -u root "$runner_container" chown -R runner:runner /workspace 2>/dev/null || true

    # Step 11: 启动本地前端
    start_frontend

    # 显示结果
    show_result
}

main "$@"
